"""Canonical LogIX integration entrypoint for attribution runs."""

from __future__ import annotations

from argparse import ArgumentParser, Namespace
import csv
from dataclasses import dataclass
from datetime import datetime, timezone
import importlib
import json
from pathlib import Path
import re
from typing import Any, Mapping, Sequence

import yaml


@dataclass(frozen=True)
class LogIXEngineConfig:
    """Configuration needed to execute a LogIX attribution run."""

    run_id: str
    output_root: Path
    model_name_or_path: str
    seed: int
    top_k: int
    train_subset_size: int
    influence_mode: str
    ihvp_controls: dict[str, float | int]
    lora_only: bool
    lora_adapter_path: Path | None
    train_manifest_path: Path | None
    setup_kwargs: dict[str, Any]
    run_kwargs: dict[str, Any]


@dataclass(frozen=True)
class LogIXRunArtifacts:
    """Paths generated by a LogIX attribution run."""

    output_dir: Path
    metadata_path: Path
    results_path: Path


def build_parser() -> ArgumentParser:
    """Build CLI parser for the LogIX engine entrypoint."""

    parser = ArgumentParser(description="Run LogIX dataset attribution pipeline")
    parser.add_argument("--config", required=True, help="Path to attribution config YAML")
    return parser


def parse_args(argv: Sequence[str] | None = None) -> Namespace:
    """Parse CLI args for LogIX execution."""

    return build_parser().parse_args(argv)


def _load_config(config_path: str | Path) -> LogIXEngineConfig:
    path = Path(config_path)
    if not path.exists():
        raise FileNotFoundError(f"Config file not found: {path}")

    with path.open("r", encoding="utf-8") as handle:
        raw = yaml.safe_load(handle) or {}

    if not isinstance(raw, dict):
        raise ValueError("Attribution config must be a mapping")

    required_keys = ("run_id", "output_root", "top_k", "train_subset_size")
    missing_required = [key for key in required_keys if key not in raw]
    if missing_required:
        raise ValueError(f"Missing required config keys: {', '.join(missing_required)}")

    run_id = str(raw.get("run_id", ""))
    if not re.fullmatch(r"[A-Za-z0-9](?:[A-Za-z0-9_.-]{0,62}[A-Za-z0-9])?", run_id):
        raise ValueError(
            "Invalid run_id. Use 1-64 chars from [A-Za-z0-9_.-], and avoid leading/trailing punctuation."
        )

    output_root = Path(str(raw.get("output_root", "")))
    if not str(output_root):
        raise ValueError("output_root must be a non-empty path")

    top_k = int(raw.get("top_k", 0))
    if top_k <= 0:
        raise ValueError("top_k must be a positive integer")

    train_subset_size = int(raw.get("train_subset_size", 0))
    if train_subset_size <= 0:
        raise ValueError("train_subset_size must be a positive integer")

    influence_cfg = raw.get("influence", {}) or {}
    if not isinstance(influence_cfg, dict):
        raise ValueError("influence must be a mapping")
    influence_mode = str(influence_cfg.get("mode", "ihvp"))
    if influence_mode != "ihvp":
        raise ValueError(f"Unsupported influence mode: {influence_mode}")

    ihvp_cfg = influence_cfg.get("ihvp", {}) or {}
    if not isinstance(ihvp_cfg, dict):
        raise ValueError("influence.ihvp must be a mapping")
    required_ihvp = ("damping", "scale", "recursion_depth", "num_samples")
    missing_ihvp = [key for key in required_ihvp if key not in ihvp_cfg]
    if missing_ihvp:
        raise ValueError(f"Missing IHVP controls: {', '.join(missing_ihvp)}")
    ihvp_controls: dict[str, float | int] = {
        "damping": float(ihvp_cfg["damping"]),
        "scale": float(ihvp_cfg["scale"]),
        "recursion_depth": int(ihvp_cfg["recursion_depth"]),
        "num_samples": int(ihvp_cfg["num_samples"]),
    }
    if ihvp_controls["recursion_depth"] <= 0 or ihvp_controls["num_samples"] <= 0:
        raise ValueError("IHVP controls recursion_depth and num_samples must be positive")

    lora_cfg = raw.get("lora", {}) or {}
    if not isinstance(lora_cfg, dict):
        raise ValueError("lora must be a mapping")
    lora_only = bool(lora_cfg.get("lora_only", True))
    adapter_path_raw = lora_cfg.get("adapter_path")
    lora_adapter_path = Path(str(adapter_path_raw)) if adapter_path_raw else None
    if lora_only:
        if lora_adapter_path is None:
            raise ValueError("lora.adapter_path is required when lora.lora_only is true")
        if not lora_adapter_path.exists():
            raise FileNotFoundError(f"LoRA adapter path not found: {lora_adapter_path}")

    logix_cfg = raw.get("logix", {})
    if logix_cfg is None:
        logix_cfg = {}
    if not isinstance(logix_cfg, dict):
        raise ValueError("logix must be a mapping")

    setup_kwargs = logix_cfg.get("setup", {}) or {}
    run_kwargs = logix_cfg.get("run", {}) or {}
    if not isinstance(setup_kwargs, dict):
        raise ValueError("logix.setup must be a mapping")
    if not isinstance(run_kwargs, dict):
        raise ValueError("logix.run must be a mapping")

    train_manifest = raw.get("train_manifest_path")
    train_manifest_path = Path(train_manifest) if train_manifest else None
    if train_manifest_path is not None and not train_manifest_path.exists():
        raise FileNotFoundError(f"Train split manifest not found: {train_manifest_path}")

    return LogIXEngineConfig(
        run_id=run_id,
        output_root=output_root,
        model_name_or_path=str(raw.get("model_name_or_path", "unknown-model")),
        seed=int(raw.get("seed", 42)),
        top_k=top_k,
        train_subset_size=train_subset_size,
        influence_mode=influence_mode,
        ihvp_controls=ihvp_controls,
        lora_only=lora_only,
        lora_adapter_path=lora_adapter_path,
        train_manifest_path=train_manifest_path,
        setup_kwargs={str(k): v for k, v in setup_kwargs.items()},
        run_kwargs={str(k): v for k, v in run_kwargs.items()},
    )


def _import_logix_module() -> Any:
    try:
        return importlib.import_module("logix")
    except ModuleNotFoundError as exc:
        raise ModuleNotFoundError(
            "Unable to import 'logix'. Install project dependencies to include the official "
            "LogIX package (logix-ai)."
        ) from exc


def _read_train_sample_ids(train_manifest_path: Path | None) -> list[str]:
    if train_manifest_path is None:
        return []
    if not train_manifest_path.exists():
        raise FileNotFoundError(f"Train split manifest not found: {train_manifest_path}")

    with train_manifest_path.open("r", encoding="utf-8", newline="") as handle:
        reader = csv.DictReader(handle)
        if "sample_id" not in (reader.fieldnames or []):
            raise ValueError("Train manifest must include sample_id column")
        return [row["sample_id"] for row in reader if row.get("sample_id")]


def setup_logix(
    config: LogIXEngineConfig,
    output_dir: Path,
    logix_module: Any,
) -> Any:
    """Prepare LogIX execution context."""

    payload = {
        "model_name_or_path": config.model_name_or_path,
        "seed": config.seed,
        "output_dir": str(output_dir),
        **config.setup_kwargs,
    }

    if hasattr(logix_module, "setup") and callable(logix_module.setup):
        return logix_module.setup(**payload)

    # Fallback context for API variants that provide run/execute only.
    return payload


def execute_logix(
    context: Any,
    config: LogIXEngineConfig,
    sample_ids: list[str],
    logix_module: Any,
) -> Mapping[str, Any]:
    """Execute LogIX run and normalize output to a mapping."""

    run_payload = {
        "context": context,
        "sample_ids": sample_ids[: config.train_subset_size],
        "top_k": config.top_k,
        "influence_mode": config.influence_mode,
        "ihvp_controls": config.ihvp_controls,
        "lora_only": config.lora_only,
        "lora_adapter_path": str(config.lora_adapter_path) if config.lora_adapter_path else None,
        **config.run_kwargs,
    }

    if hasattr(logix_module, "run") and callable(logix_module.run):
        raw_result = logix_module.run(**run_payload)
    elif hasattr(logix_module, "execute") and callable(logix_module.execute):
        raw_result = logix_module.execute(**run_payload)
    else:
        raise AttributeError(
            "Unsupported LogIX API surface. Expected module-level `setup` and `run` "
            "(or `execute`) callables."
        )

    if raw_result is None:
        return {"status": "ok", "num_samples": len(sample_ids)}
    if not isinstance(raw_result, Mapping):
        raise ValueError("LogIX run result must be a mapping")
    return dict(raw_result)


def run_logix_engine(config_path: str | Path, logix_module: Any | None = None) -> LogIXRunArtifacts:
    """Run LogIX with config-driven setup and persist reproducible artifacts."""

    config = _load_config(config_path)
    if logix_module is None:
        logix_module = _import_logix_module()

    run_root = config.output_root / config.run_id
    output_dir = run_root / "logix"
    output_dir.mkdir(parents=True, exist_ok=True)

    sample_ids = _read_train_sample_ids(config.train_manifest_path)
    context = setup_logix(config, output_dir=output_dir, logix_module=logix_module)
    result = execute_logix(context, config=config, sample_ids=sample_ids, logix_module=logix_module)

    result_payload = {
        "run_id": config.run_id,
        "num_samples": len(sample_ids),
        "results": result,
    }
    results_path = output_dir / "results.json"
    results_path.write_text(json.dumps(result_payload, indent=2), encoding="utf-8")

    metadata = {
        "timestamp": datetime.now(timezone.utc).isoformat(),
        "model_name_or_path": config.model_name_or_path,
        "seed": config.seed,
        "top_k": config.top_k,
        "train_subset_size": config.train_subset_size,
        "influence_mode": config.influence_mode,
        "ihvp_controls": config.ihvp_controls,
        "lora_only": config.lora_only,
        "lora_adapter_path": str(config.lora_adapter_path) if config.lora_adapter_path else None,
        "train_manifest_path": str(config.train_manifest_path) if config.train_manifest_path else None,
        "setup_kwargs": config.setup_kwargs,
        "run_kwargs": config.run_kwargs,
        "results_path": str(results_path),
    }
    metadata_path = output_dir / "metadata.json"
    metadata_path.write_text(json.dumps(metadata, indent=2), encoding="utf-8")

    return LogIXRunArtifacts(
        output_dir=output_dir,
        metadata_path=metadata_path,
        results_path=results_path,
    )


def main(argv: Sequence[str] | None = None) -> None:
    """CLI entrypoint for `python -m src.attribution.logix_engine --config ...`."""

    args = parse_args(argv)
    artifacts = run_logix_engine(args.config)
    print(f"LogIX attribution finished. Outputs written to: {artifacts.output_dir}")


if __name__ == "__main__":
    main()
